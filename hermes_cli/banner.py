def _format_context_length(tokens: int) -> str:
    """Format a token count for display (e.g. 128000 ‚Üí '128K', 1048576 ‚Üí '1M')."""
    if tokens >= 1_000_000:
        val = tokens / 1_000_000
        return f"{val:g}M"
    elif tokens >= 1_000:
        val = tokens / 1_000
        return f"{val:g}K"
    return str(tokens)


